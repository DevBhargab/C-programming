#include <stdio.h>

int sum(int a, int b){
    return a+b;
}
int sun(int a, int b){
    return a-b;
}
int sub(int a, int b){
    return a*b;
}
int sup(int a, int b){
    return a/b;
}
 int main (){
int ans = sum(5,4);
printf("%d\n",ans);
int ans1 = sun(5,4);
printf("%d\n",ans1);
int ans2 = sub(5,4);
printf("%d\n",ans2);
int ans3 = sup(5,4);
printf("%d\n",ans3);
}

